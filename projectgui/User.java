package projectgui;

public class User {
    private String name;
    private int empID;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }


    public void setEmpID(int empID) {
        this.empID = empID;
    }
    public int getEmpID() {
        return empID;
    }
}
